# Introduction to lists part 6 , how to sort the list
list_letters = ['s', 't', 'i', 'n', 'k', 'y']
print(list_letters)
list_letters.sort()
print(list_letters)
list_letters.sort(reverse=True)
print(list_letters)

list_numbers = ['1', '8', '3', '2']
print(list_numbers)
list_numbers.sort()
print(list_numbers)
list_numbers.sort(reverse=True)
print(list_numbers)
